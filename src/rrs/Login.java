    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUser.getText();
        String password = new String(txtPass.getText());
        
        String sql = "SELECT * FROM users WHERE username = ?";
        try (Connection conn = DBConnection.Connect()) {
            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setString(1, username);
                ResultSet rs = ps.executeQuery();
                
                if (rs.next()) {
                    String storedPass = rs.getString("password");
                    String name = rs.getString("first_name") + " " + rs.getString("last_name");
                    if (storedPass.equals(password)) {
                        JOptionPane.showMessageDialog(this, "Welcome! " + name + "!", "Login Successful", JOptionPane.INFORMATION_MESSAGE);
                        Main main = new Main();
                        main.setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Incorrect password. Please try Again.", "Login Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "User not found.", "Login Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException e) {
            showErrorMessage("Database Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

